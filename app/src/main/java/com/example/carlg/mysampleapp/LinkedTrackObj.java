package com.example.carlg.mysampleapp;

class LinkedTrackObj {
    private ExternalURL external_urls;
    private String href;
    private String id;
    private String type;
    private String uri;

    /* Accessor methods for JSON object Track Link */
    public ExternalURL getExternalUrls() {
        return external_urls;
    }

    public String getHref() {
        return href;
    }

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getUri() {
        return uri;
    }
}
